class Solution {
  public:
    void deleteMid(stack<int>& s) {
        int n=s.size()/2;
        stack<int> st;
        while (n-->=0){
            st.push(s.top());
            s.pop();
        }
        st.pop();
        while (!st.empty()){
            s.push(st.top());
            st.pop();
        }
    }
};

